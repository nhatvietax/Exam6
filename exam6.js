// Chu Vi
function chuvi(dai, rong) {
    return chuvi = (dai + rong) * 2;
}
// Dien tich
function dientich(dai, rong) {
    return dientich = (dai * rong);
}
//
let dai = 4;
let rong = 2;
let tinhchuvi = chuvi(dai, rong);
let tinhdientich = dientich(dai, rong);
//
console.log("Chu vi HCN la : " + tinhchuvi);
console.log("Dien tich HCN la : " + tinhdientich);